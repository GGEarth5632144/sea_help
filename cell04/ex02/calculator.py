#!/usr/bin/env python3
one = int(input("Give me the first number: "))
two = int(input("Give me the second number: "))
print("Thank you!")
print(f"{one} + {two} = {one + two}")
print(f"{one} - {two} = {one - two}")
print(f"{one} / {two} = {one / two}")
print(f"{one} * {two} = {one * two}")
print(f"{one} % {two} = {one % two}")